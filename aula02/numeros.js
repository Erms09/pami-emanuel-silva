// Snake case é  caracterizado por utilizar o underline/underscore (_) 
// para separar as palavras, como por exemplo: numero_um, numero_dois, etc.
let primeiro_valor = 0
let segundo_valor = 0

primeiro_valor = 4
segundo_valor = 5

// - , / e *

console.log(
    `${primeiro_valor} + ${segundo_valor} =`,
    primeiro_valor + segundo_valor
)

console.log(
    `${primeiro_valor} - ${segundo_valor} =`,
    primeiro_valor - segundo_valor
)

console.log(
    `${primeiro_valor} * ${segundo_valor} =`,
    primeiro_valor * segundo_valor
)

console.log(
    `${primeiro_valor} / ${segundo_valor} =`,
    primeiro_valor / segundo_valor
)
